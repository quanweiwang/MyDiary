#import "NSAttributedString+MDDiary.h"

@implementation NSAttributedString (MDDiary)

+(NSMutableAttributedString *) strikethrough:(NSString *)str{

    NSMutableAttributedString *attributedString = [[NSMutableAttributedString alloc] initWithString:str];
    NSRange strRange = {0,[str length]};
    [attributedString addAttribute:NSStrikethroughStyleAttributeName value:@(0x0000 | 0x01) range:strRange];
    return attributedString;
}

@end
